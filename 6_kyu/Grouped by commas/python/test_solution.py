import pytest

from solution import group_by_commas


data = [
    (1, "1"),
    (10, "10"),
    (100, "100"),
    (1000, "1,000"),
    (10000, "10,000"),
    (100000, "100,000"),
    (1000000, "1,000,000"),
    (35235235, "35,235,235"),
    (12, "12"),
    (123, "123"),
    (1234, "1,234"),
    (12345, "12,345"),
    (123456, "123,456"),
    (1234567, "1,234,567"),
    (12345678, "12,345,678"),
    (123456789, "123,456,789"),
    (1234567890, "1,234,567,890"),
]


@pytest.mark.parametrize("n, result", data)
def test_group_by_commas(n, result):
    assert group_by_commas(n) == result
