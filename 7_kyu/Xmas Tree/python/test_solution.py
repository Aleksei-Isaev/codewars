import pytest

from solution import xmastree


tests = [
    (3, ['__#__', '_###_', '#####', '__#__', '__#__']),
    (7, ['______#______', '_____###_____', '____#####____', '___#######___', '__#########__', '_###########_', '#############', '______#______', '______#______']),
    (2, ['_#_', '###', '_#_', '_#_']),
    (4, ['___#___', '__###__', '_#####_', '#######', '___#___', '___#___']),
    (6, ['_____#_____', '____###____', '___#####___', '__#######__', '_#########_', '###########', '_____#_____', '_____#_____']),
]


@pytest.mark.parametrize(
    "n, expected", tests
)
def test_should_build_a_xmas_tree(n, expected):
    assert xmastree(n) == expected
